let broj1=parseInt(prompt("Внеси го првиот број"))
let operacija=prompt("Внеси една математичка операција (+,-;*,/)")
let broj2=parseInt(prompt("Внеси го вториот број"))
if(operacija=="+"){
  alert(broj1+broj2)
}
else if(operacija=="-"){
  alert(broj1-broj2)
}
else if(operacija=="*"){
  alert(broj1*broj2)
}
else if(operacija=="/"){
  alert(broj1/broj2)
}